    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
       
        String synID = jList1.getSelectedValue();
        
        Synset s = loader.getSynset(synID);
        
        jTextField1.setText(s.ID);
        jTextField2.setText(s.POSTag);
        jTextField3.setText(s.Definition);
        jTextField4.setText(s.Example);
        
        dlm2.removeAllElements();
        for(HashMap.Entry<String,Integer> pair:s.Synonyms.entrySet()){
            dlm2.addElement(pair.getKey()+": "+pair.getValue());
        }
        
        dlm3.removeAllElements();
        for(HashMap.Entry<String,String> pair:s.SemanticRelations.entrySet()){
            dlm3.addElement(pair.getKey()+": "+pair.getValue());
        }
        
        DefaultTableModel dtm = (DefaultTableModel)jTable1.getModel();
        while(dtm.getRowCount()>0)dtm.removeRow(0);
        int order = 1;
        for(HashMap.Entry<String,String> pair:s.EnglishSemanticRelations.entrySet()){
             dtm.addRow(new Object[]{order++, pair.getKey(), pair.getValue()});     
        }
        
        
        jTextField5.setText("");
        jTextField6.setText("");
        dlm4.removeAllElements();
        jTextArea1.setText("");
        jTextArea2.setText("");
    }//GEN-LAST:event_jList1ValueChanged

